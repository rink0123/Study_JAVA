package Variables;

/**
 * <h3>변수 요약(Summary of Variables)</h3>
 * 
 * <h4>참고 사이트</h4>
 * <ul>
 * <li><a href="https://docs.oracle.com/javase/tutorial/java/nutsandbolts/variablesummary.html">
 * Java Tutorials > Language Basics > Variables > Summary of Variables</a></li>
 * </ul>
 * 
 * @since JDK 11
 * @version 1.0.0.RELEASE
 * @author 강민성
 */
public class SummaryVariables {
	/*
	 * 1. Java 프로그래밍 언어는 용어의 일부로 "필드(fields)"와 "변수(variables)"를 모두 사용한다.
	 * 2. 인스턴스 변수 (비정적 필드, non-static field)는 클래스의 각 인스턴스에 고유하다.
	 * 3. 클래스 변수(정적 필드, static field)는 static으로 선언된 필드이다.
	 * 4. 클래스가 인스턴스화 된 횟수에 관계 없이 각 클래스 변수들은 고유하다.
	 * 5. 지역 변수는 임시 상태를 저장하며, 메서드 내에서 선언된다.
	 * 6. 지역 변수와 매개 변수는 항상 "변수(variables)"(not "fields")로 분류한다.
	 * 7. 필드 또는 변수의 이름을 지정할 때 따라야 하는 규칙이 있다.
	 * 8. Java 프로그래밍 언어에서 지원하는 8가지 기본 자료형은 byte, short, int, long, float, double, boolean, char.
	 * 9. 문자열은 java.lang.String 클래스로 표시된다.
	 * 10. 컴파일러는 기본 자료형의 필드에 적절한 기본값을 할당한다. 지역 변수의 경우 기본값이 할당되지 않는다.
	 * 11. 리터럴(literal)은 고정 값의 소스 코드 표현이다.
	 * 12. 배열(array)은 단일 유형의 값의 고정된 수를 보관하는 컨테이너 객체이다.
	 * 13. 배열의 길이는 배열이 생성될 때 설정해야 하며, 생성 후 길이는 고정된다.
	 */
}
